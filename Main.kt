val animals = arrayOf(camel, lion, deer, goose, bat, rabbit)
var exitProgram = false

fun main() {
    println("My first implementation using Kotlin - to help the local zoo look after its denizens : )\nEnable Interactive mode")
    while (!exitProgram) {
        showMenu()
    }
}

fun showMenu() {
    println("\nPlease enter the habitat number you would like to view")
    println("Options: 0-camel, 1-lion, 2-deer, 3-goose, 4-bat, 5-rabbit or exit")
    when (val input = readLine()!!) {
        "0", "1", "2", "3", "4", "5" -> println(animals[input.toInt()])
        "exit" -> {
            println("See you later!");
            exitProgram = true
        }
    }
}
